	public static function friendly_title($str)
	{
	  return strtolower(preg_replace(array('/[^a-zA-Z0-9 -]/', '/[ -]+/', '/^-|-$/'), 
	  array('', '-', ''), static::_remove_accent($str)));
	}

	public static function render($type='all')
	{
		switch($type)
		{
			case 'all':
				$return = '';
				$site = model_generator_preparer::$currentSite;

				if(!empty($site))
					$return .= self::_getSeoData($site);

				$return .= self::_getAnalytics();
				return $return;
			break;
			case 'head':
				$site = model_generator_preparer::$currentSite;

				if(!empty($site))
					return self::_getSeoData($site);
			break;
			case 'analytics':
				return self::_getAnalytics();
			break;
		}
	}
