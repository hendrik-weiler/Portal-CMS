class model_generator_seo
{

	private static function _getSeoData($site)
	{
		if(!model_generator_module::$seo_head)
			return;
			
		$title = (empty($site->site_title) ? $site->label : $site->site_title);

		$titleformat = model_db_option::getKey('titleformat')->value;

		$titlecompleted = str_replace('{sitename}', $title, $titleformat);

		$keywords = stripslashes($site->keywords);
		$description = $site->description;

		$keywordsTag = '';
		$descriptionTag = '';
		if(!empty($keywords)) {
			$keywordsTag = '<meta name="keywords" content="' . $keywords . '">';
		}
		if(!empty($description)) {
			$descriptionTag = '<meta name="description" content="' . $description . '">';
		}

		return '<title>' . stripslashes($titlecompleted) . '</title>'
			 . 	$keywordsTag
			 .  $descriptionTag
			 . '<base href="' . Uri::create('/') . '" />'
			 #. '<meta http-equiv="Content-Language" content="' . model_generator_preparer::$lang . '" />'
			 #. '<meta name="language" content="' . model_generator_preparer::$lang . '" />'
			 . '<meta name="robots" content="' . stripslashes(model_db_option::getKey('robots')->value) . '" />';
	}

	private static function _getAnalytics()
	{
		if(!model_generator_module::$seo_analytics)
			return;
			
		$id = model_db_option::getKey('analytics_id')->value;
		return "<script>
				var _gaq = _gaq || [];
				_gaq.push (['_setAccount', '" . stripslashes($id) . "']);
				_gaq.push (['_gat._anonymizeIp']);
				_gaq.push (['_trackPageview']);

				(function() {var ga = document.createElement('script'); 
					ga.type = 'text/javascript'; ga.async = true; ga.src = ('https:' == document.location.protocol ? 'https://ssl' : 'http://www') + '.google-analytics.com/ga.js'; 
					var s = document.getElementsByTagName('script')[0]; s.parentNode.insertBefore(ga, s);
				})();
				</script>";
	}

